
public class Produto {
        private String nome;
        private int codigo;
        private double preco;
        
        
      Produto(String nome , int codigo, double preco){
    	  this.nome = nome;
    	  this.codigo = codigo;
    	  this.preco = preco;
      }
        
  
        
public String toString() {
	return "\n\nNome:" + nome +
           "\nCodigo:" + codigo + 
           "\nPreco:" + preco + "R$";
	 
} 
        
        
        
        
        
        
        
        
        
        
        
        
             

        
}
